function startFaceTracking() {
  if (!faceDetector || !webcamPreview) return;

  const loop = async () => {
    if (webcamPreview.readyState >= 2) {
      try {
        const faces = await faceDetector.detect(webcamPreview);

        if (faces && faces.length > 0) {
          // ì—¬ëŸ¬ ëª… ì¤‘ ê°€ì¥ ê°€ê¹Œìš´ ì–¼êµ´(ë©´ì  ê°€ì¥ í° bounding box) ì„ íƒ
          let mainFace = null;
          let maxArea = 0;
          for (const f of faces) {
            const box = f.boundingBox;
            const area = box.width * box.height;
            if (area > maxArea) {
              maxArea = area;
              mainFace = f;
            }
          }

          if (mainFace) {
            const box = mainFace.boundingBox;
            const videoWidth = webcamPreview.videoWidth || webcamPreview.clientWidth || 1;
            const centerX = box.x + box.width / 2;
            const norm = (centerX / videoWidth) * 2 - 1; // -1(ì™¼ìª½) ~ 1(ì˜¤ë¥¸ìª½)

            let newDir = 'center';
            if (norm < -0.15) newDir = 'left';
            else if (norm > 0.15) newDir = 'right';

            eyeDirection = newDir;
            updatePortraitFrame();

            setFaceStatus(`eyes: ${eyeDirection} / ì–¼êµ´ ì¸ì‹ì¤‘`);
          }
        } else {
          // ì–¼êµ´ ì—†ìŒ â†’ ì²œì²œíˆ ì¤‘ì•™ìœ¼ë¡œ ë³µê·€
          if (eyeDirection !== 'center') {
            eyeDirection = 'center';
            updatePortraitFrame();
          }
          setFaceStatus('eyes: center / ì–¼êµ´ ì¸ì‹ ë¶ˆê°€ (í”„ë ˆì„ ë‚´ì— ì—†ìŒ)');
        }
      } catch (e) {
        console.error('FaceDetector ì—ëŸ¬:', e);
        setFaceStatus(`eyes: ${eyeDirection} / ì–¼êµ´ ì¸ì‹ ì—ëŸ¬`);
      }
    }
    requestAnimationFrame(loop);
  };

  requestAnimationFrame(loop);
}

