function centerXFromLandmarks(landmarks){
  const ids = [30, 33, 27, 8]; // ÏΩî/ÌÑ± Ï£ºÎ≥Ä
  let sum = 0, n = 0;
  for (const i of ids){
    const pt = landmarks.positions[i];
    if (pt){ sum += pt.x; n++; }
  }
  return n ? (sum / n) : null;
}

