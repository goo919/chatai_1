function typeWriter(element, text, delay = 16, onChar = null, onDone = null) {
  element.textContent = '';
  let i = 0;
  (function tick() {
    if (i < text.length) {
      const ch = text.charAt(i);
      element.textContent += ch;
      playBeep(220 + (ch.charCodeAt(0) % 220));
      if (typeof onChar === 'function') onChar(ch);   // üîÅ ÎπÑÌîÑÏôÄ ÎèôÍ∏∞ÌôîÎêú ÏΩúÎ∞±
      i++;
      setTimeout(tick, delay);
    } else {
      if (typeof onDone === 'function') onDone();
    }
  })();
}

function splitAndTypeWriter(element, text, maxLength = 160, delay = 16, onChar = null, onAllDone = null) {
  const words = text.split(' ');
  const parts = [];
  let part = '';

  for (const w of words) {
    if ((part + (part ? ' ' : '') + w).length > maxLength) {
      parts.push(part);
      part = w;
    } else {
      part += (part ? ' ' : '') + w;
    }
  }
  if (part) parts.push(part);

  (async () => {
    for (let idx = 0; idx < parts.length; idx++) {
      const p = parts[idx];
      const line = document.createElement('div');   // ‚úÖ Ï§Ñ Îã®ÏúÑ DIV ÏÉùÏÑ±
      element.appendChild(line);
      await new Promise(resolve => {
        typeWriter(
          line,
          p,
          delay,
          onChar,                      // üîÅ Í∞Å Í∏ÄÏûê ÏΩúÎ∞± Ï†ÑÎã¨
          () => setTimeout(resolve, 20) // Ï§Ñ ÏÇ¨Ïù¥ ÏïΩÍ∞ÑÏùò Î≤ÑÌçº
        );
      });
    }
    if (typeof onAllDone === 'function') onAllDone();
  })();
}

