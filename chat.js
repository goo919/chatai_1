const HANGUL_BASE = 0xAC00;
const HANGUL_LAST = 0xD7A3;
// Ï§ëÏÑ± Ïù∏Îç±Ïä§ 0..20: „Öè,„Öê,„Öë,„Öí,„Öì,„Öî,„Öï,„Öñ,„Öó,„Öò,„Öô,„Öö,„Öõ,„Öú,„Öù,„Öû,„Öü,„Ö†,„Ö°,„Ö¢,„Ö£
