function handleFacesFromNative(faces) {
  if (!faces.length) {
    setFaceStatus('얼굴 인식 불가');
    setEyeDebug('CENTER');
    eyeDirection = 'center';
    updatePortraitFrame();
    return;
  }

  // 가장 큰 얼굴 선택
  let best = faces[0];
  let bestArea = (best.boundingBox.width || 0) * (best.boundingBox.height || 0);
  for (let i = 1; i < faces.length; i++) {
    const f = faces[i];
    const area = (f.boundingBox.width || 0) * (f.boundingBox.height || 0);
    if (area > bestArea) {
      best = f;
      bestArea = area;
    }
  }

  const box = best.boundingBox;
  const cx = box.x + box.width / 2;
  const vw = videoEl.videoWidth || videoEl.clientWidth || 1;
  const ratio = cx / vw;

  if (ratio < 0.4) {
    eyeDirection = 'left';
    setEyeDebug('LEFT');
  } else if (ratio > 0.6) {
    eyeDirection = 'right';
    setEyeDebug('RIGHT');
  } else {
    eyeDirection = 'center';
    setEyeDebug('CENTER');
  }
  setFaceStatus('얼굴 인식중');
  updatePortraitFrame();
}

